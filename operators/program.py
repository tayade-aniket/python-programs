# Arithmetic Operators
a = 10
b = 5

add = a + b
sub = a - b
mul = a * b
div = a / b
mod = a % b
pow = a ** b

print(add)
print(sub)
print(mul)
print(div)
print(mod)
print(pow)

# Logical Operators
a = True
b = False

print(a and b)
print(a or b)
print(not a)

# Bitwise Operators
a = 10
b = 4

print(a & b)
print(a | b)
print(~a)
print(a ^ b)
print(a >>2)
print(a<<2)


# Assignment Operators
a = 10

b = a
print(b)

b += a
print(b)

b -= a
print(b)

b *= a
print(b)

b <<= a
print(b)