# Control Statement/Control Flow 
# If condition
age = int(input("Please enter your age : "));

if(age > 18):
    print("You can Vote.")
print("Execution is Completed !")


# If_Else condition
age = int(input("Please enter your age : "));

if(age > 18):
    print("You can Vote.")
else:
    print("You cannot Vote.")
